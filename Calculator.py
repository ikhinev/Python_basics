def add(a, b):
    """
    Add two numbers.

    Parameters:
    a : The first number.
    b : The second number.

    Returns:
    The sum of the two numbers.
    """
    return a + b

def subtract(a, b):
    """
    Subtracts the second number from the first number.

    Parameters:
    a: The first number.
    b: The second number.

    Returns:
    The difference between the two numbers.
    """
    return a - b

def multiply(a, b):
    """
    Multiply two numbers.

    Parameters:
    a: The first number.
    b: The second number.

    Returns:
    The product of the two numbers.
    """
    return a * b
  
